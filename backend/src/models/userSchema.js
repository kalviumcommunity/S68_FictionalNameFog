const mongoose = require("mongoose");

const User = mongoose.Schema({
    name: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
        unique: true,
    },
})

const userModel = mongoose.model("User", User);

module.exports = userModel;